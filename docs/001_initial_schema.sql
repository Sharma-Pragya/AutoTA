-- =============================================================
-- VIEWS for common queries
-- =============================================================

-- Student's current attempt status for an assignment
CREATE VIEW IF NOT EXISTS v_student_attempt_status AS
SELECT
    a.student_id,
    a.assignment_id,
    a.attempt_number,
    a.status,
    a.started_at,
    a.submitted_at,
    ar.total_score,
    ar.problems_correct,
    ar.problems_total,
    asn.max_attempts,
    CASE WHEN a.attempt_number < asn.max_attempts AND a.status = 'graded' THEN 1 ELSE 0 END AS can_retry
FROM attempts a
JOIN assignments asn ON a.assignment_id = asn.id
LEFT JOIN attempt_results ar ON a.id = ar.attempt_id;

-- Full grade report: student × problem × grade for an assignment
CREATE VIEW IF NOT EXISTS v_grade_report AS
SELECT
    s.id AS student_id,
    s.name AS student_name,
    a.assignment_id,
    a.attempt_number,
    p.parent_label,
    p.sub_label,
    p.points AS problem_points,
    sub.answer_raw,
    g.score,
    g.correct,
    g.feedback,
    g.grading_tier
FROM submissions sub
JOIN attempts a ON sub.attempt_id = a.id
JOIN students s ON a.student_id = s.id
JOIN problems p ON sub.problem_id = p.id
LEFT JOIN grades g ON sub.id = g.submission_id;
